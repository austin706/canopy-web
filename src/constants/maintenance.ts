import type { TaskFrequency, TaskPriority, EquipmentCategory, TaskSchedulingType } from '@/types';
import { Colors } from './theme';

export type ClimateRegion = 'south' | 'north' | 'mountain' | 'coastal' | 'desert' | 'midwest' | 'all';

export interface TaskTemplate {
  id: string;
  title: string;
  description: string;
  instructions: string[];
  category: EquipmentCategory | 'general' | 'lawn' | 'pool' | 'deck' | 'seasonal' | 'pest_control' | 'fireplace';
  priority: TaskPriority;
  frequency: TaskFrequency;
  scheduling_type: TaskSchedulingType; // 'dynamic' = next due relative to completion, 'seasonal' = fixed calendar months
  interval_days?: number; // for dynamic tasks: days between occurrences
  applicable_months: number[]; // for seasonal tasks: which months; for dynamic: used only on first generation
  estimated_minutes: number;
  estimated_cost: number;
  estimated_pro_cost?: number;
  requires_equipment?: EquipmentCategory;
  requires_home_feature?: string; // e.g., 'has_pool', 'has_deck'
  requires_countertop_type?: string[]; // specific countertop types that trigger this task
  service_purpose?: string;
  items_to_have_on_hand?: string[];
  applicable_regions?: ClimateRegion[]; // undefined = all regions
  pro_responsible?: boolean; // true = pro provider handles this during visits; false/undefined = homeowner DIY
  is_cleaning?: boolean; // true = cleaning/tidying task that can be toggled off
  /** C-11 (migration 066): auto-bumped when admin edits the template. Snapshotted
   *  onto generated tasks so we can detect and refresh stale tasks. Absent for
   *  hardcoded TASK_TEMPLATES (only DB-backed templates carry a version). */
  template_version?: number;

  // ─── Consumable-aware scheduling (Migration 041) ───
  /**
   * If true, this template is keyed off an equipment_consumables row
   * rather than the parent equipment. One task is generated per
   * matching consumable (e.g., one per HVAC return filter).
   */
  equipment_keyed?: boolean;
  /**
   * Consumable type this template corresponds to. When
   * equipment_keyed=true, only generate for consumables with this
   * `consumable_type` value (e.g., 'filter', 'belt', 'anode_rod').
   */
  consumable_spec?:
    | 'filter'
    | 'belt'
    | 'igniter'
    | 'anode_rod'
    | 'battery'
    | 'bulb'
    | 'blade'
    | 'gasket'
    | 'fuse'
    | 'other';
  /**
   * Default replacement interval in months when the consumable row
   * doesn't specify one. Overridden by
   * equipment_consumables.replacement_interval_months when present.
   */
  consumable_replacement_months?: number;

  // ─── Branched scheduling (Migration 040) ───
  /**
   * Restricts this template to homes with a specific pool_type.
   * e.g., ['chlorine','salt'] for general pool tasks,
   * ['salt'] for "check salt cell" tasks,
   * ['chlorine'] for "shock pool" tasks.
   */
  requires_pool_type?: Array<'chlorine' | 'salt' | 'mineral' | 'none'>;

  // ─── Infrastructure type filtering ───
  /**
   * Restricts this template to homes with a specific water source.
   * e.g., ['well'] for well-only tasks
   */
  requires_water_source?: Array<'municipal' | 'well'>;
  /**
   * Restricts this template to homes with a specific sewer type.
   * e.g., ['septic'] for septic-only tasks
   */
  requires_sewer_type?: Array<'municipal' | 'septic'>;
  /**
   * Restricts this template to homes with a specific septic system type.
   * Only checked when sewer_type = 'septic'. e.g., ['aerobic'] for aerobic-only tasks
   */
  requires_septic_type?: Array<'aerobic' | 'conventional'>;
  /**
   * Restricts this template to homes with a specific home type.
   * e.g., ['single_family', 'townhome'] to exclude condos/apartments
   */
  requires_home_type?: string[];
  /**
   * Restricts this template to homes with a matching primary_flooring value.
   * e.g., ['carpet'] for carpet-only tasks, ['hardwood', 'engineered_wood'] for wood floor tasks
   */
  requires_flooring_type?: string[];
  /**
   * Restricts to homes with matching construction_type (OR logic with requires_foundation_type).
   * e.g., ['wood_frame', 'log'] for termite-prone construction
   */
  requires_construction_type?: string[];
  /**
   * Restricts to homes with matching foundation_type (OR logic with requires_construction_type).
   * e.g., ['crawlspace', 'pier_and_beam'] for termite-prone foundations
   * When BOTH requires_construction_type and requires_foundation_type are set, the task generates
   * if EITHER condition matches (OR logic — the home is at risk if it has wood frame OR crawlspace).
   */
  requires_foundation_type?: string[];
  /**
   * Restricts this template to equipment with a matching equipment_subtype.
   * Case-insensitive partial match: if the equipment's subtype includes any
   * of these strings, the template applies.
   * e.g., ['tank'] matches "Tank Water Heater" but not "Tankless Water Heater"
   */
  requires_equipment_subtype?: string[];
  /**
   * Excludes this template when the equipment subtype matches any of these
   * strings (case-insensitive partial match).
   * e.g., ['tankless'] excludes "Tankless Water Heater"
   */
  excludes_equipment_subtype?: string[];

  /** Visibility tier: core = basic/essential, standard = recommended, comprehensive = everything */
  task_level?: 'core' | 'standard' | 'comprehensive';

  // ─── Service classification (Pro Service Audit, April 2026) ───
  /**
   * How this task is fulfilled:
   *   'visit'  — handled by the Canopy pro during bimonthly visits
   *   'add_on' — requires a licensed/specialized provider (available as add-on subscription)
   *   'diy'    — homeowner does it themselves
   * When omitted, defaults to 'diy'.
   */
  service_type?: 'visit' | 'add_on' | 'diy';

  /**
   * Which add-on category this task falls under.
   * Only meaningful when service_type = 'add_on'.
   */
  add_on_category?: 'hvac' | 'pest' | 'chimney' | 'septic' | 'irrigation' | 'cleaning' | 'generator' | 'well' | 'lawn' | 'pool';

  /**
   * Structured safety warnings. Rendered as a red-bordered panel on TaskDetail.
   * Authored by admin via the how-to editor (migration 061 / AdminReferenceData).
   * The hardcoded TASK_TEMPLATES array doesn't set this; it's populated per-template
   * in the DB once an admin edits the template.
   */
  safety_warnings?: string[];
}

